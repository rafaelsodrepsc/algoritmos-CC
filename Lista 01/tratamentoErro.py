def erroVirgula(NumeroVirg):
    return NumeroVirg.replace(',','.')

def DivisaoZero(denominador):
    if denominador == 0 :
        return print('Divisão por zero invalida')
    
def ignoreGraus(graus):
    return graus.replace('°' ,'')
    
def ignorePorcent(Porcent):
    return Porcent.replace('%','')

